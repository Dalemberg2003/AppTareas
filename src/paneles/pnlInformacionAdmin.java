    public pnlInformacionAdmin() {
        initComponents();
        fila = new Object[2];
        tbl_informacion = new JTable(model);
        jScrollPane2.setViewportView(tbl_informacion);
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("select * from cuentasusuarios");
            ResultSet rs = pst.executeQuery();
            model.addColumn("Usuario");
            model.addColumn("Contrase√±a");            
            while (rs.next()) {
                for (int i = 0; i < 2; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                model.addRow(fila);
            }
            cn.close();
        } catch (SQLException e) {
            System.err.println("Error al extraer los datos " + e);
        }
        
        tbl_informacion.addMouseListener(new MouseAdapter(){
            
            @Override
            public void mouseClicked(MouseEvent e){
                int fila_point = tbl_informacion.rowAtPoint(e.getPoint());                
                int columna_usuario = 0;
                if(fila_point > -1){
                    usuario = (String)model.getValueAt(fila_point, columna_usuario);
                    ModificarInformacionAdmin modificarInfo = new ModificarInformacionAdmin();
                    modificarInfo.setVisible(true);
                }
            }
        });

    }

