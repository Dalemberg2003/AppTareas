    private void btn_enviar_reclamoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_enviar_reclamoMouseClicked
       mensaje = texarea_reclamos.getText().trim();
       if(mensaje.equals("")){
           JOptionPane.showMessageDialog(this, "Debe ingresar su reclamo","Error",JOptionPane.ERROR_MESSAGE);
           texarea_reclamos.requestFocus();
       }else{
           try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("insert into reclamos (cliente,mensaje) values (?,?)");
            pst.setString(1, user);
            pst.setString(2, mensaje);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Reclamo Enviado","Reclamos",JOptionPane.INFORMATION_MESSAGE);
            texarea_reclamos.setText("");
            cn.close();
        } catch (SQLException e) {
            System.err.println("Error al registrar el reclamo "+e);
        }
       }        
    }//GEN-LAST:event_btn_enviar_reclamoMouseClicked


