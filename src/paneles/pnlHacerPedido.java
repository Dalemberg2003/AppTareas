    private void btn_enviarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_enviarMouseClicked

        if (txt_contactoCelular.getText().equals("") && txt_contactoCorreo.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Debe agregar una forma de contacto");
        } else {
            if (txt_archivo.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Debe agregar el archivo de la tarea");
            } else {
                if (txtArea.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Por favor ingrese un mensaje (proporcionar fecha de entrega)");
                } else {
                    //enviando datos a la base de datos
                    //en proceso - aceptado - terminado - cancelado
                    cliente = txt_cliente.getText().trim();
                    numero = txt_contactoCelular.getText().trim();
                    correo = txt_contactoCorreo.getText().trim();
                    mensaje = txtArea.getText().trim();
                    estado = "en proceso";
                    if(numero.equals("")){
                        numero = "VACIO";
                    }
                    if(correo.equals("")){
                        correo = "VACIO";
                    }
                    try {
                        Connection cn = Conexion.conectar();
                        PreparedStatement pst = cn.prepareStatement("insert into tareasusuarios (cliente,celular,correo,archivo,nombreArchivo,mensaje,estado) values (?,?,?,?,?,?,?)");
                        pst.setString(1, cliente);                        
                        pst.setString(2, numero);                        
                        pst.setString(3, correo);
                        pst.setBinaryStream(4, fis, (int) fichero.length());
                        pst.setString(5, nombreArchivo);
                        pst.setString(6, mensaje);
                        pst.setString(7, estado);
                        pst.executeUpdate();
                        limpiar();
                        JOptionPane.showMessageDialog(this, "Tarea recibida, Nos pondremos en contacto con usted pronto!", "Tarea", JOptionPane.INFORMATION_MESSAGE);
                    } catch (SQLException e) {
                        System.err.println("Error al enviar los datos a la base de datos " + e);
                        JOptionPane.showMessageDialog(this, "El archivo excede el tamaño permitido", "Tarea", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        }
    }//GEN-LAST:event_btn_enviarMouseClicked


